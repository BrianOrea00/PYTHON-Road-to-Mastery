# Casting
# there will be a time that we want to specify a type on a variable.
# this can be done by casting.
# python is a object oriented language and in such it uses classes to define data types.
# casting in python is therefore done using constructor functions
# what is constructor?
# constructor is a special function that runs automatically when you create an object from a class.
# we have int() construct an integer number from an integer literal, a float literal (by removing all decimals), or string literal (providing the string represents a whole number).
# we have float() construct an float number frim an integer literal, a float literal or string literal (providing  the string represents a float or a integer).
# we have str() construct an string from wide variety of data types, including string, integer literal and float literal.
# example1(integer)
x1 = int(1)
x2 = int(2.9999999999)
x3 = int("3")
print(x1 ,x2 ,x3)

#example2(float)
y1 = float(1)
y2 = float(2.2)
y3 = float("3")
y4 = float("4.1")
print(y1 ,y2 ,y3, y4)

#example3(string)
z1 = str("qwer")
z2 = str(2)
z3 = str(3.0)
print(z1 ,z2, z3)

