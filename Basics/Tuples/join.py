# Join Tuple
# Join Two Tuples
# to join two or more tuples we can use the "+" operator
tuple1 = ("a", "b", "c")
tuple2 = (1, 2, 3)
tuple3 = tuple1 + tuple2
print(tuple3)

# Multiply Tuples
# if we want to multiply the content of a tuple a given number of times we can use the "*" operator
fruits = ("apple", "banana", "cherry")
mulFruits = fruits * 2
print(mulFruits)
