# Assign Multiple value
# in here we can assign a Multiple variable and have Multiple data in one line
# example1
x1, y1, z1 = "first data", "second data", "third data"
print(x1)
print(y1)
print(z1)

# in the second example we can assign 1 data in Multiple variable using equal sign(=)
# example2
x2 = y2 = z2 = "Same data"
print(x2)
print(y2)
print(z2)

# we have here a interesting one the unpacking
# this one we can extract the data in a list , tuple etc to give it to Multiple variable
# example3
Food = ["Hotdog","Pizza","Burger"]
x3, y3, z3 = Food
print(x3)
print(y3)
print(z3)
