# Output Variable
# the print() function is often used to output Variable
# example1
x = "this is Output Variable"
print(x)

# we can print multiple variable using coma ( , )
# example2
a = "this is"
b = "Output"
c = "Variable"
print(a, b, c)

# we can also print multiple variable using plus sign ( + )
# example3
d = "I "
f = "am "
g = "Learning"
print(d + f + g)

# additional info as you can see in the variables d and f there is a space after the letter or word because if we dont put space in it it will combine in one sentence
# if we didnt put space in it the output will be "IamLearning"
# we can also add numbers 
# example4
