# Concatenation
# to concatenate or combine two string we can use the "+" operation
# example1
# we will merge varriable a with variable b into variable c.
a = "Hello"
b = "World!"
c = a + b
print(c)

# or we can add double quote to have whitespace between variable a and variable b.
# we will use variable a and variable b.
d = a + " " + b
print(d)
