# Logical Operator
# logical operators are used to combine conditional statements
# we have "and" returns true if both statements are true
x = 1
print(x < 5 and x < 10)

# we have "or" returns true if one of the statements is true
y = 4
print(y < 1 or y < 5)

# we have "not" reverse the result returns false if the result is true
z = 3
print(not(z > 5 and z < 10))
