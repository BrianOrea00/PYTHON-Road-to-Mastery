# Comparison
# comparison operators are used to compare two values.

x = 5
y = 3

# we have "==" EQUAL
print(x == y)

# we have "!=" NOT EQUAL
print(x != y)

# we have ">" GREATER THAN
print(x > y)

# we have "<" LESS THAN
print(x < y)

# we have ">=" GREATER THAN OR EQUAL TO
print(x >= y)

# we have "<=" LESS THAN OR EQUAL TO
print(x <= y)

# Chaining comparison operators
# python allows us to chain comparison operators.

z = 5
print(1 < z < 10)
print(1 < x and x < 10)
