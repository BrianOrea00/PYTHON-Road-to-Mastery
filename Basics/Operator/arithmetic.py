# Arithmetic
# arithmetic operator are used with numeric values to perform common mathematical operations.
a = 10
b = 10

# we have "+" addition
print(a + b)

# we have "-" subtraction
print(a - b)

# we have "*" multiplication
print(a * b)

# we have "/" division
print(a / b)

# we have "%" modulus
c = 11
print(c % a)

# we have "**" exponentiation
print(a ** b) # same as 10*10*10*10*10*10*10*10*10*10

# we have "//" floor division
d = 15
e = 2
print(d // e) #the floor division // rounds the result down to the nearest whole number

# division
# python has two division operators: "/" returns a float and "//" returns an integer
